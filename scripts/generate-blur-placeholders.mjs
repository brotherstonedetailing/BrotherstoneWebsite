import fs from "node:fs/promises";
import path from "node:path";
import { getPlaiceholder } from "plaiceholder";

const IMAGE_PATHS = [
  "/hero.jpg",
  "/aboutUs.jpg",
  "/m4thumbnail.png",
  "/m4interior.png",
  "/m4exterior.png",
  "/PorcheThumbNail.jpeg",
  "/PorscheInterior.jpeg",
  "/PorscheExterior.jpeg",
];

async function generateBlurPlaceholders() {
  const blurs = {};

  for (const imagePath of IMAGE_PATHS) {
    const filePath = path.join(process.cwd(), "public", imagePath.slice(1));
    const buffer = await fs.readFile(filePath);
    const { base64 } = await getPlaiceholder(buffer);
    blurs[imagePath] = base64;
  }

  const output = `// Generated by scripts/generate-blur-placeholders.mjs
// Re-run after adding or changing images in /public

export const IMAGE_BLURS: Record<string, string> = ${JSON.stringify(blurs, null, 2)};
`;

  await fs.writeFile(
    path.join(process.cwd(), "app/lib/image-blurs.ts"),
    output,
    "utf8",
  );

  console.log(`Generated blur placeholders for ${IMAGE_PATHS.length} images.`);
}

generateBlurPlaceholders().catch((error) => {
  console.error(error);
  process.exit(1);
});
